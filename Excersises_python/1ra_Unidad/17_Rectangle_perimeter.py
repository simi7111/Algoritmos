#Este programa calcula el perimetro de un rectangulo
lado=float(input("Dame la medida de un lado del rectangulo: "))
lado2=float(input("Dame la medida de el otro lado del rectangulo: "))
perimeter_s=(lado*2)+(lado2*2)
print("El perimetro de un rectangulo en el que sus lado miden",lado,"y",lado2,"es", perimeter_s)